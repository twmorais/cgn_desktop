    private void preencherTabelas() {

        DefaultTableModel model_despesas = (DefaultTableModel) tbl_despesas.getModel();
        model_despesas.setRowCount(0);
        DefaultTableModel model_receitas = (DefaultTableModel) tbl_receitas.getModel();
        model_receitas.setRowCount(0);

        for (Pedido p : pedidos) {
            String tipo;
            String data;
            String total;
            tipo = p.getTipo();
            data = converte_data_pedido(p.getStr_data());
            total = NumberFormat.getCurrencyInstance().format(p.getTotal());
            model_receitas.addRow(new Object[]{tipo, data, total});
        }
        for (Pedido p : pedidos) {
            total_pedidos = total_pedidos + p.getTotal();
        }
        for (Despesa d : despesas) {
            String tipo;
            String data;
            String total;
            tipo = d.getTipo();
            data = converte_data_pedido(d.getData_despesa());
            total = NumberFormat.getCurrencyInstance().format(d.getTotal());
            model_despesas.addRow(new Object[]{tipo, data, total});
        }

        for (Despesa d : despesas) {
            total_despesas = total_despesas + d.getTotal();
        }
    }

    private void datas_para_pesquisa() {

        String dia_inicio = txt_data_inicial.getText().substring(0, 2);
        String mes_inicio = txt_data_inicial.getText().substring(3, 5);
        String ano_inicio = txt_data_inicial.getText().substring(6);
        inicio = ano_inicio +  mes_inicio + dia_inicio;

        String dia_fim = txt_data_final.getText().substring(0, 2);
        String mes_fim = txt_data_final.getText().substring(3, 5);
        String ano_fim = txt_data_final.getText().substring(6);
        fim = ano_fim +  mes_fim +  dia_fim;

    }

    private String converte_data_pedido(String str_data) {

        String dia_inicio = str_data.substring(8);
        String mes_inicio = str_data.substring(5, 7);
        String ano_inicio = str_data.substring(0, 4);
        String data_formatada = dia_inicio + "/" + mes_inicio + "/" + ano_inicio;
        return data_formatada;
    }

    private void calcula_lucro() {

        total = total_pedidos - total_despesas;
    }
