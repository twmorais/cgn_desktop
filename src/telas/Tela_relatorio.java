    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed

        total_despesas = 0.00;
        total_pedidos = 0.00;
        DespesaDao despesaDao = new DespesaDao();
        PedidoDao pedidoDao = new PedidoDao();

        if (txt_data_inicial.getText().trim().equals("") || txt_data_final.getText().trim().equals("")) {
            JOptionPane.showInternalMessageDialog(null, "Erro, favor insira as datas para realizar a pesquisa:");
        } else {

            try {
                datas_para_pesquisa();
                despesas = despesaDao.buscarDespesaPorData(inicio, fim);
                pedidos = pedidoDao.buscarTodos(inicio, fim);
                preencherTabelas();
                txt_total_despesas.setText(NumberFormat.getCurrencyInstance().format(total_despesas));
                txt_total_receitas1.setText(NumberFormat.getCurrencyInstance().format(total_pedidos));
                calcula_lucro();
                if (total < 0) {
                    txt_resultado.setText("Prejuízo: " + String.valueOf(NumberFormat.getCurrencyInstance().format(total)));
                }
                if (total > 0) {
                    txt_resultado.setText("Lucro: " + String.valueOf(NumberFormat.getCurrencyInstance().format(total)));
                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Tela_relatorio.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btn_buscarActionPerformed

