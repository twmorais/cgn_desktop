    private void btn_buscar_pelo_tipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscar_pelo_tipoActionPerformed

        String tipo_despesa = txt_busca_por_tipo.getText();
        DespesaDao despesaDao = new DespesaDao();
        Despesa despesa = new Despesa();

        DefaultTableModel model = (DefaultTableModel) table_editar_Despesa.getModel();
        model.setRowCount(0);

        if (txt_busca_por_tipo.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor, digite um tipo de despesa para realizar a busca.");
            limpar();
        } else {

            try {
                despesas = despesaDao.buscarDespesa(tipo_despesa);

                if (despesas.isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Despesa não encontrada.");
                    txt_busca_por_tipo.setText("");
                    limpar();
                }
            } catch (ClassNotFoundException ex) {
                JOptionPane.showInternalMessageDialog(null, "Erro, favor contatar o Administrador do sistema:");
            }

            atualizar_tbl_despesa();
        }

    }//GEN-LAST:event_btn_buscar_pelo_tipoActionPerformed

