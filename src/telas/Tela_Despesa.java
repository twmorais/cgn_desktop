    private void atualizar_tbl_despesa() {

        String busca_despesa = txt_busca_por_tipo.getText();
        DespesaDao despesaDao = new DespesaDao();
        Despesa despesa = new Despesa();
        DefaultTableModel model = (DefaultTableModel) table_editar_Despesa.getModel();
        model.setRowCount(0);

        if (txt_busca_por_tipo.getText().trim().equals("")) {
        } else {
            try {
                despesas = despesaDao.buscarDespesa(busca_despesa);

            } catch (ClassNotFoundException ex) {
                JOptionPane.showInternalMessageDialog(null, "Erro, favor contatar o Administrador do sistema:");
            }
            for (Despesa d : despesas) {
                String tipo;
                String descricao;
                String valor;
                String data_despesa;
                d.setData_despesa(datas_vindas_mysql(d));
                tipo = d.getTipo();
                descricao = d.getDescricao();
                data_despesa = d.getData_despesa();
                valor = NumberFormat.getCurrencyInstance().format(d.getTotal());
                model.addRow(new Object[]{tipo, descricao, valor, data_despesa});
            }
        }
    }

    boolean validar_inserir_atualizar() {

        String datas = datas_para_mysql();

        if (datas != "") {
            if (txt_tipo.getText().trim().equals("") || txt_preco.getText().trim().equals("")) {
                JOptionPane.showInternalMessageDialog(null, "Erro, Insira todos os dados:");
                return false;
            } else {
                try {
                    String data_atual = datas_para_mysql();
                    despesa.setTipo(txt_tipo.getText());
                    despesa.setDescricao(txt_descricao.getText());
                    despesa.setData_despesa(data_atual);
                    despesa.setTotal(Float.valueOf(txt_preco.getText()));
                    return true;
                } catch (NumberFormatException ex) {
                    JOptionPane.showInternalMessageDialog(null, "Erro, Dados inválidos:");
                    return false;
                }
            }
        }
        return false;
    }
